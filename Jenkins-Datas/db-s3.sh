#!/bin/bash

DATE=$(date +%H-%M-%S)
DB_HOST=$1
DB_PASSWORD=$2
DB_NAME=$3
AWS_SECRET=$4
BUCKET_NAME=$5
DB_DUMP_FILE_NAME=$DB_NAME-db-$DATE.sql

mysqldump -u root -h $DB_HOST -p$DB_PASSWORD $DB_NAME > /tmp/$DB_DUMP_FILE_NAME

export AWS_ACCESS_KEY_ID=AKIAVOIPMNUDFUAQQF7S
export AWS_SECRET_ACCESS_KEY=$AWS_SECRET
export AWS_DEFAULT_REGION=eu-central-1

aws s3 cp /tmp/$DB_DUMP_FILE_NAME s3://$BUCKET_NAME
